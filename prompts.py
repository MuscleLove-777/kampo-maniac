"""漢方マニアック - プロンプト定義

漢方特化ブログ用のプロンプトを一元管理する。
YMYL対策: 出典明記、体験ベース、断定表現回避
薬機法対策: 「効く」→「使われる」「期待される」の表現統一
"""


def build_keyword_prompt(config):
    """キーワード選定プロンプトを構築する"""
    categories_text = "\n".join(f"- {cat}" for cat in config.TARGET_CATEGORIES)
    return (
        "漢方薬の専門ブログ用のキーワードを選定してください。\n\n"
        "以下のカテゴリから1つ選び、そのカテゴリで検索需要が高い"
        "漢方関連のキーワードを1つ提案してください。\n\n"
        f"カテゴリ一覧:\n{categories_text}\n\n"
        "【キーワード選定の基準】\n"
        "- ロングテールキーワード（3語以上の組み合わせ）を優先\n"
        "- 症状×漢方薬名、体質×症状、ツムラ番号×処方名など具体的な組み合わせ\n"
        "- 「漢方 ○○ 効果」「ツムラ ○○番」「○○ vs ○○」などの比較・疑問系\n"
        "- 季節性のあるキーワードも考慮（花粉症、冷え性、夏バテなど）\n\n"
        "以下の形式でJSON形式のみで回答してください（説明不要）:\n"
        '{"category": "カテゴリ名", "keyword": "キーワード"}'
    )


def build_article_prompt(keyword, category, config):
    """漢方マニアック記事生成プロンプトを構築する"""
    return f"""あなたは漢方薬に精通した薬学研究者であり、
一般の方にも漢方の奥深さを伝えられるサイエンスライターです。

以下のキーワードについて、マニアックかつ実用的な記事を書いてください。

【基本条件】
- ブログ名: {config.BLOG_NAME}
- キーワード: {keyword}
- カテゴリ: {category}
- 言語: 日本語
- 文字数: {config.MAX_ARTICLE_LENGTH}文字程度

【記事フォーマット】
## この記事の結論
- 結論1（最も重要なポイント）
- 結論2
- 結論3

## 漢方の視点で解説
（気・血・水、証（虚実）、五臓の概念を使って解説。
専門用語には（）で中学生にもわかる補足説明を付ける）

## 具体的な処方と使い分け
（関連する漢方薬を複数挙げ、それぞれの特徴と使い分けを表形式で整理。
ツムラ番号がある場合は必ず記載。例：「葛根湯（ツムラ1番）」）

## 知っておきたい注意点
（副作用、併用注意、服用タイミング、こんな人は医師に相談など）

## マニアックメモ
（この処方の歴史的背景、原典の記載、生薬の産地情報など、
他のサイトでは読めないディープな情報）

## まとめ：こんな人におすすめ
（どんな体質・症状の人に向いているか、簡潔に整理）

【YMYL・薬機法対策（最重要）】
- 「～に効く」「治る」「改善する」などの断定表現は絶対に使わない
- 代わりに「～に用いられる」「～の改善が期待される」「～に処方されることが多い」を使う
- 「※個人の感想であり、効果を保証するものではありません」の注記を適切に挿入
- 「症状が続く場合は医師・薬剤師にご相談ください」を記事末に必ず記載
- 情報の根拠として添付文書、ツムラ公式、論文などの出典を明記

【SEO要件】
1. タイトルにキーワード「{keyword}」を必ず含めること
2. タイトルは32文字以内で魅力的に
3. H2、H3の見出し構造を適切に使用すること
4. キーワード密度は{config.MIN_KEYWORD_DENSITY}%〜{config.MAX_KEYWORD_DENSITY}%を目安に
5. メタディスクリプションは{config.META_DESCRIPTION_LENGTH}文字以内

【差別化ポイント】
- ツムラ番号を必ず記載（例：防風通聖散（ツムラ62番））
- 処方の横比較を入れる（ツムラ vs クラシエの違い、類似処方の使い分けなど）
- 体質タイプ（気虚・血虚・陰虚・気滞・瘀血・水滞）を明記
- 具体的な数字やデータを含める（処方数ランキング、価格帯など）
- 内部リンクのプレースホルダーを2〜3箇所に配置（{{{{internal_link:関連トピック}}}}の形式）

【出力形式】
以下のJSON形式で出力してください。JSONブロック以外のテキストは出力しないでください。

```json
{{
  "title": "SEO最適化されたタイトル",
  "content": "# タイトル\\n\\n本文（Markdown形式）...",
  "meta_description": "120文字以内のメタディスクリプション",
  "tags": ["タグ1", "タグ2", "タグ3", "タグ4", "タグ5"],
  "slug": "url-friendly-slug"
}}
```

【注意事項】
- content内のMarkdownは適切にエスケープしてJSON文字列として有効にすること
- tagsは5個ちょうど生成すること
- slugは半角英数字とハイフンのみ使用すること
- 漢方の専門用語には必ず（カッコ）で平易な補足説明を付けること
- 他サイトにはないマニアックな情報を必ず1つ以上含めること"""
